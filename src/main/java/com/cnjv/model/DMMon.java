package com.cnjv.model;

public class DMMon {
	private String idDMMon;
	private String tenDMMon;
	
	public DMMon() {
	}
	
	public DMMon(String idDMMon, String tenDMMon) {
		super();
		this.idDMMon = idDMMon;
		this.tenDMMon = tenDMMon;
	}

	public String getIdDMMon() {
		return idDMMon;
	}

	public void setIdDMMon(String idDMMon) {
		this.idDMMon = idDMMon;
	}

	public String getTenDMMon() {
		return tenDMMon;
	}

	public void setTenDMMon(String tenDMMon) {
		this.tenDMMon = tenDMMon;
	}
	
	

}
